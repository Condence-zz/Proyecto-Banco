    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        borrarBusqueda();
        saldo = Double.parseDouble(jTextField1.getText()); 
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel(); 
        Object rowData[] = new Object[5]; 
        
        NodoLista nodo = lista.getCabecera().getSiguiente();
        int count=0;
        while (nodo != null) {
            Cliente p = (Cliente) nodo.getElemento();  
            if (p.getSaldo() == saldo) { 
                Object[] fila = {p.getId(),p.getEdad(),p.getNombre(),p.getApellido(), p.getSaldo()};
                nodo = nodo.getSiguiente();
                model.addRow(fila); 
            } else {
                nodo = nodo.getSiguiente();
            } 
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    public static void borrarBusqueda() {
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount(); 
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
    }
